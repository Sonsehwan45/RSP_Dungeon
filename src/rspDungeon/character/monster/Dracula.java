    @Override
    public void attack(Character target) {
        if(target.currentHP - power <= 0){
            target.currentHP = 0;
            System.out.println("------------------------------------------------------------------------");
            System.out.println(target.name + "이(가) 사망했습니다.");
            System.out.println("------------------------------------------------------------------------");
        }
        else{
            target.currentHP -= power;
            if(this.currentHP + 10 <= maxHP){
                currentHP += 10;
            }else{
                currentHP = maxHP;
            }
            System.out.println("------------------------------------------------------------------------");
            System.out.println(target.name + "이(가) " + power + "만큼 데미지를 입습니다.");
            System.out.println(target.name + "의 현재 HP : " + target.currentHP);
            System.out.println(name + "가 HP를 10 흡혈합니다.");
            System.out.println(name + "의 현재 HP : " + currentHP);
            System.out.println("------------------------------------------------------------------------");
        }
    }

    //회복(비긴 경우)
    public void heal(){
        if((currentHP + resilience/2) <= maxHP){
            currentHP += resilience/2;
        }else{
            currentHP = maxHP;
        }
        System.out.println("------------------------------------------------------------------------");
        System.out.println(name + "의 현재 HP : " + currentHP);
        System.out.println("------------------------------------------------------------------------");
    }
